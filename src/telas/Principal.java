    public void excluir() {

        if (tbMoto.getSelectedRow() != -1) {
            ModelCadastro motoBean = new ModelCadastro();
            DAOCadastro dao = new DAOCadastro();

            motoBean.setPlaca((String) tbMoto.getValueAt(tbMoto.getSelectedRow(), 0));

            dao.deletar(motoBean);
           // carregarDados();
            carregarDados2();
            //pesquisaNome(txtPesquisa.getText());
            //estoque();
            JOptionPane.showMessageDialog(null, "Excluido com sucesso");

        } else {
            JOptionPane.showMessageDialog(null, "Selecione para apagar");
        }

    }

